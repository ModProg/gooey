#[must_use]
pub fn map_input_event(
    winit::KeyEvent {
        physical_key,
        logical_key,
        text,
        location,
        repeat,
        ..
    }: winit::KeyEvent,
    modifiers: winit::Modifiers,
) -> KeyEvent {
    KeyEvent {
        physical_key: map_key_code(physical_key),
        logical_key: map_key(logical_key),
        text,
        location: map_key_location(location),
        repeat,
        modifiers: map_modifiers(modifiers),
    }
}

