fn map_native_key_code(key_code: winit::NativeKeyCode) -> NativeKeyCode {
    match key_code {
        winit::NativeKeyCode::Unidentified => NativeKeyCode::Unidentified ,
        winit::NativeKeyCode::Android(k) => NativeKeyCode::Android(k) ,
        winit::NativeKeyCode::MacOS(k) => NativeKeyCode::MacOS(k) ,
        winit::NativeKeyCode::Windows(k) => NativeKeyCode::Windows(k) ,
        winit::NativeKeyCode::Xkb(k) => NativeKeyCode::Xkb(k) ,
    }
}

