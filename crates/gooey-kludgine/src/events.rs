fn map_native_key(key: winit::NativeKey) -> NativeKey {
    match key {
        winit::NativeKey::Unidentified => NativeKey::Unidentified,
        winit::NativeKey::Android(k) => NativeKey::Android(k),
        winit::NativeKey::MacOS(k) => NativeKey::MacOS(k),
        winit::NativeKey::Windows(k) => NativeKey::Windows(k),
        winit::NativeKey::Xkb(k) => NativeKey::Xkb(k),
        winit::NativeKey::Web(k) => NativeKey::Web(k),
    }
}

fn map_key_location(location: winit::KeyLocation) -> KeyLocation {
    match location {
        winit::KeyLocation::Standard => KeyLocation::Standard,
        winit::KeyLocation::Left => KeyLocation::Left,
        winit::KeyLocation::Right => KeyLocation::Right,
        winit::KeyLocation::Numpad => KeyLocation::Numpad,
    }
}

fn map_modifiers(modifiers: winit::Modifiers) -> Modifiers {
    let mut state = ModifierState::default();
    if modifiers.state().shift_key() {
        state |= ModifierState::SHIFT;
    }
    if modifiers.state().alt_key() {
        state |= ModifierState::ALT;
    }
    if modifiers.state().control_key() {
        state |= ModifierState::CTRL;
    }
    if modifiers.state().super_key() {
        state |= ModifierState::SUPER;
    }

    let mut keys = ModifierKeys::default();
    if modifiers.lshift_state() == ModifiersKeyState::Pressed {
        keys |= ModifierKeys::LSHIFT;
    }
    if modifiers.rshift_state() == ModifiersKeyState::Pressed {
        keys |= ModifierKeys::RSHIFT;
    }
    if modifiers.lalt_state() == ModifiersKeyState::Pressed {
        keys |= ModifierKeys::LALT;
    }
    if modifiers.ralt_state() == ModifiersKeyState::Pressed {
        keys |= ModifierKeys::RALT;
    }
    if modifiers.lsuper_state() == ModifiersKeyState::Pressed {
        keys |= ModifierKeys::LSUPER;
    }
    if modifiers.rsuper_state() == ModifiersKeyState::Pressed {
        keys |= ModifierKeys::RSUPER;
    }
    if modifiers.lcontrol_state() == ModifiersKeyState::Pressed {
        keys |= ModifierKeys::LCTRL;
    }
    if modifiers.rcontrol_state() == ModifiersKeyState::Pressed {
        keys |= ModifierKeys::RCTRL;
    }

    Modifiers { state, keys }
}
